"""
Prometheus metrics definitions for AIDRA.

All metrics follow the naming convention `aidra_<subsystem>_<metric>_<unit>`.
Labels are used for dimensionality (profile, model_variant, status, etc.).

Cardinality policy (I-TRACE-3)
------------------------------
The execution UUID (``run_id``) is intentionally **not** a label. Storing
millions of unique label-value combinations would explode Prometheus
TSDB cardinality. Instead the run_id is attached as an **OpenMetrics
exemplar** to ``aidra_pipeline_runs_total`` and
``aidra_inference_duration_seconds``. Exemplars are exposed only when
clients request the OpenMetrics content-type
(``application/openmetrics-text``); plain Prometheus scrape stays
cardinality-free.

This closes invariant I-TRACE-3 (run_id end-to-end across logs +
metrics) without sacrificing scrape stability.

Usage:
    from src.observability.prometheus_metrics import (
        PIPELINE_RUNS_TOTAL, DETECTIONS_TOTAL, INFERENCE_DURATION,
        generate_metrics_response,
    )

    PIPELINE_RUNS_TOTAL.labels(
        profile="ground", model_variant="yolov8n", status="success"
    ).inc(exemplar={"trace_id": str(execution_id)})

    with INFERENCE_DURATION.labels(profile="ground", model_variant="yolov8n").time():
        result = run_inference(image)
"""

