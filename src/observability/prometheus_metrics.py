def generate_metrics_response(
    accept_header: str | None = None,
) -> tuple[bytes, str]:
    """Generate the Prometheus metrics exposition payload.

    When the caller advertises support for OpenMetrics via the
    ``Accept`` header (e.g. ``application/openmetrics-text``), the
    response uses the OpenMetrics 1.0.0 format so that exemplars
    (the ``run_id`` / ``trace_id`` link) are visible. Otherwise the
    classic Prometheus text format is used (no exemplars).

    Returns:
        A tuple of ``(body_bytes, content_type)`` suitable for returning from
        an HTTP handler.  Example usage in FastAPI::

            @app.get("/api/metrics")
            async def metrics_endpoint(request: Request):
                accept = request.headers.get("accept")
                body, content_type = generate_metrics_response(accept)
                return Response(content=body, media_type=content_type)
    """
    if accept_header and "application/openmetrics-text" in accept_header:
        body: bytes = openmetrics_generate_latest(REGISTRY)
        return body, OPENMETRICS_CONTENT_TYPE
    body = generate_latest(REGISTRY)
    return body, CONTENT_TYPE_LATEST
